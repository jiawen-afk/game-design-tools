  assert.match(actionsSource, /export function createPersonalSpaceAssetActions/)
  assert.match(actionsSource, /exportStoryboardWithStatus/)
  assert.match(actionsSource, /uploadCharacterPortrait/)
  assert.match(actionsSource, /uploadCharacterSprite/)
