  assert.match(actionsSource, /deleteAsset/)
  assert.match(actionsSource, /portraitUploadProps/)
  assert.match(actionsSource, /spriteUploadProps/)
  assert.match(actionsSource, /voiceUploadProps/)
  assert.match(actionsSource, /storyboardVoiceUploadProps/)
  assert.match(actionsSource, /commonResourceUploadProps/)
  assert.match(actionsSource, /imageSpriteUploadProps/)
  assert.match(actionsSource, /createPersonalSpaceUploadProps/)
  assert.doesNotMatch(actionsSource, /createSpriteUploadBatch/)
  assert.doesNotMatch(actionsSource, /consumeSpriteUploadBatch/)
  assert.doesNotMatch(actionsSource, /createRecordSpriteUploadBatchTracker/)
  assert.doesNotMatch(actionsSource, /createNullableSpriteUploadBatchTracker/)
