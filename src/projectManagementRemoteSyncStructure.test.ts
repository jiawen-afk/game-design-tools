test('remote project realtime sync is debounced and has a periodic fallback', () => {
  const workspaceSource = readFileSync('src/components/PersonalSpaceWorkspace/usePersonalSpaceWorkspace.ts', 'utf8')
  const lifecycleSource = readFileSync('src/components/PersonalSpaceWorkspace/usePersonalSpaceWorkspaceLifecycle.ts', 'utf8')
  const remoteSyncSource = readFileSync('src/components/PersonalSpaceWorkspace/useProjectRemoteSync.ts', 'utf8')

  assert.match(remoteSyncSource, /REMOTE_PROJECT_SYNC_DEBOUNCE_MS/)
  assert.match(remoteSyncSource, /REMOTE_PROJECT_PERIODIC_SYNC_MS/)
  assert.match(remoteSyncSource, /const remoteSyncTimerRef = useRef/)
  assert.match(remoteSyncSource, /const queuedRemoteSyncRef = useRef\(createProjectRemoteSyncQueue<PersonalSpaceState>\(\)\)/)
  assert.match(remoteSyncSource, /const pending = new Map<string, QueuedProjectRemoteSync<State>>\(\)/)
  assert.match(remoteSyncSource, /const remoteSyncInFlightRef = useRef\(false\)/)
  assert.match(remoteSyncSource, /const optionsRef = useRef\(options\)/)
  assert.match(remoteSyncSource, /optionsRef\.current = options/)
  assert.match(remoteSyncSource, /const flushQueuedRemoteSync = async \(\)/)
  assert.match(remoteSyncSource, /const scheduleRemoteProjectSync = \(\s*nextSpace: PersonalSpaceState,\s*delay = REMOTE_PROJECT_SYNC_DEBOUNCE_MS,/)
  assert.match(remoteSyncSource, /remoteSyncTimerRef\.current = window\.setTimeout/)
  assert.match(remoteSyncSource, /void flushQueuedRemoteSync\(\)/)
  assert.match(remoteSyncSource, /\}, delay\)/)
  assert.match(remoteSyncSource, /remoteSyncIntervalRef\.current = window\.setInterval/)
  assert.match(remoteSyncSource, /scheduleRemoteProjectSync\(optionsRef\.current\.getCurrentSpace\(\), 0\)/)
  assert.match(remoteSyncSource, /\}, REMOTE_PROJECT_PERIODIC_SYNC_MS\)/)
  assert.match(workspaceSource, /usePersonalSpaceWorkspaceLifecycle/)
  assert.match(lifecycleSource, /scheduleRemoteProjectSync\(space\)/)
  assert.doesNotMatch(workspaceSource, /remoteSyncIntervalRef/)
  assert.doesNotMatch(workspaceSource, /queuedRemoteSync/)
  assert.doesNotMatch(remoteSyncSource, /queuedRemoteSyncSpaceRef/)
  assert.doesNotMatch(workspaceSource, /void syncActiveProjectStateToStorage\(space\)/)
})
